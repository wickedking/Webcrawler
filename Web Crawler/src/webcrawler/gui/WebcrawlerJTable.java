package webcrawler.gui;

import javax.swing.JTable;

/**
 * A simple JTable that does not have editable cells.
 * 
 * @author Cody Shafer 
 *
 */
@SuppressWarnings("serial")
public class WebcrawlerJTable extends JTable {
	
	public WebcrawlerJTable(){
		super();
	}
	
	public WebcrawlerJTable(Object[][] rowData, Object[] columnNames){
		super(rowData, columnNames);
	}
	
	@Override 
	public boolean isCellEditable(int row, int column){
		return false;
	}

}
